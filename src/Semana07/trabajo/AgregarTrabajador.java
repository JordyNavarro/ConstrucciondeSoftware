    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
        String nombre=txtnombre.getText();
        String apellido=txtapellido.getText();
        String puesto=jcpuesto.getSelectedItem().toString();
        int dni=Integer.parseInt(txtdni.getText());
        float sueldo=Float.valueOf(txtsueldo.getText());
        mostrar.registro.addTrabajador(new Trabajador(nombre, apellido, dni, puesto, sueldo));
        txtnombre.setText("");
        txtnombre.requestFocus();
        txtapellido.setText("");
        txtapellido.requestFocus();
        txtdni.setText("");
        txtdni.requestFocus();
        txtsueldo.setText("");
        txtsueldo.requestFocus();       
               
    }//GEN-LAST:event_btnagregarActionPerformed

    private void btnmostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmostrarActionPerformed
        setVisible(false);
        mostrar.setVisible(true);
    }//GEN-LAST:event_btnmostrarActionPerformed

    public void btnatrasActionPerformed(java.awt.event.ActionEvent evt){
        if(evt.getSource()==mostrar.btnatras){
            setVisible(true);
            mostrar.setVisible(false);
            mostrar.txtaDatos.setText("");
        }
            
    }
    
    private void jcpuestoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcpuestoActionPerformed
        if(jcpuesto.getSelectedIndex()==0){
            txtsueldo.setText("5000");
        }
        if(jcpuesto.getSelectedIndex()==1){
            txtsueldo.setText("4000");
        }
        if(jcpuesto.getSelectedIndex()==2){
            txtsueldo.setText("2500");
        }
        if(jcpuesto.getSelectedIndex()==3){
            txtsueldo.setText("1200");
        }
    }//GEN-LAST:event_jcpuestoActionPerformed

