class Estudiante{
    int nro;
    int n1,n2,n3;
    double prom;
    
    public Estudiante(int nro,int n1,int n2,int n3){
        this.nro=nro;
        this.n1=n1;
        this.n2=n2;
        this.n3=n3;
        this.prom=(double)(n1+n2+n3)/3;
    }

    public int getNro() {
        return nro;
    }

    public void setNro(int nro) {
        this.nro = nro;
    }

    public int getN1() {
        return n1;
    }

    public void setN1(int n1) {
        this.n1 = n1;
    }

    public int getN2() {
        return n2;
    }

    public void setN2(int n2) {
        this.n2 = n2;
    }

    public int getN3() {
        return n3;
    }

    public void setN3(int n3) {
        this.n3 = n3;
    }

    public double getProm() {
        return prom;
    }

    public void setProm(double prom) {
        this.prom = prom;
    }
    
    
}

